package com.kyk.mealtracker.bot;

import com.kyk.mealtracker.entity.BotUser;
import com.kyk.mealtracker.entity.Meal;
import com.kyk.mealtracker.repository.BotUserRepository;
import com.kyk.mealtracker.services.AdminService;
import com.kyk.mealtracker.services.MealService;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;
import org.telegram.telegrambots.bots.TelegramLongPollingBot;
import org.telegram.telegrambots.meta.api.methods.send.SendMessage;
import org.telegram.telegrambots.meta.api.objects.Update;
import org.telegram.telegrambots.meta.api.objects.User;
import org.telegram.telegrambots.meta.exceptions.TelegramApiException;

import java.time.LocalDate;
import java.time.LocalDateTime;
import java.time.format.DateTimeFormatter;
import java.util.List;
import java.util.Locale;

@Component
public class KykMealBot extends TelegramLongPollingBot {

    private static final Logger logger = LoggerFactory.getLogger(KykMealBot.class);
    private final MealService mealService;
    private final BotUserRepository botUserRepository;
    private final AdminService adminService;

    @Value("${telegram.bot.username}")
    private String botUsername;

    @Value("${telegram.bot.token}")
    private String botToken;

    public KykMealBot(MealService mealService, BotUserRepository botUserRepository, AdminService adminService) {
        this.mealService = mealService;
        this.botUserRepository = botUserRepository;
        this.adminService = adminService;
    }

    @Override
    public String getBotUsername() {
        return botUsername;
    }

    @Override
    public String getBotToken() {
        return botToken;
    }

    @Override
    public void onUpdateReceived(Update update) {
        if (!update.hasMessage() || !update.getMessage().hasText()) {
            return;
        }

        String messageText = update.getMessage().getText();
        Long chatId = update.getMessage().getChatId();
        User user = update.getMessage().getFrom();

        try {
            // Her mesajda kullanƒ±cƒ±yƒ± kaydet/g√ºncelle
            saveOrUpdateUser(chatId, user);

            // Admin komutlarƒ± kontrol√º
            if (messageText.startsWith("/admin_")) {
                if (!adminService.isAdmin(chatId)) {
                    sendMessage(chatId, "üö´ Bu komutu kullanma yetkiniz bulunmuyor.");
                    return;
                }
                handleAdminCommand(chatId, messageText);
                return;
            }

            switch (messageText) {
                case "/start":
                    sendWelcomeMessage(chatId);
                    break;
                case "/bugun":
                    sendTodaysMeals(chatId);
                    break;
                case "/yarin":
                    sendTomorrowsMeals(chatId);
                    break;
                case "/bildirim_ac":
                    enableNotifications(chatId);
                    break;
                case "/bildirim_kapat":
                    disableNotifications(chatId);
                    break;
                case "/yardim":
                    sendHelpMessage(chatId);
                    break;
                case "/stats":
                    if (adminService.isAdmin(chatId)) {
                        sendMessage(chatId, adminService.getBotStats());
                    } else {
                        sendMessage(chatId, "üö´ Bu komutu kullanma yetkiniz bulunmuyor.");
                    }
                    break;
                default:
                    sendMessage(chatId, "‚ùì Komut anla≈üƒ±lamadƒ±. Yardƒ±m i√ßin /yardim yazabilirsiniz.");
            }
        } catch (Exception e) {
            logger.error("Message handling error for chatId: " + chatId, e);
            try {
                sendMessage(chatId, "‚ö†Ô∏è Bir hata olu≈ütu. L√ºtfen daha sonra tekrar deneyin.");
            } catch (TelegramApiException ex) {
                logger.error("Error sending error message", ex);
            }
        }
    }

    private void handleAdminCommand(Long chatId, String command) throws TelegramApiException {
        String[] parts = command.split(" ", 2);
        String cmd = parts[0];

        switch (cmd) {
            case "/admin_list":
                sendMessage(chatId, adminService.getUserList(0));
                break;
            case "/admin_broadcast":
                if (parts.length < 2) {
                    sendMessage(chatId, "‚ö†Ô∏è Kullanƒ±m: /admin_broadcast [mesaj]\n\n√ñrnek:\n/admin_broadcast Bug√ºn yemekhanede bakƒ±m √ßalƒ±≈ümasƒ± yapƒ±lacaktƒ±r.");
                    return;
                }
                int userCount = broadcastMessage(parts[1]);
                sendMessage(chatId, "‚úÖ Mesaj ba≈üarƒ±yla g√∂nderildi!\nüë• Toplam " + userCount + " kullanƒ±cƒ±ya ula≈ütƒ±rƒ±ldƒ±.");
                break;
            case "/admin_stats":
                sendMessage(chatId, adminService.getBotStats());
                break;
            default:
                sendMessage(chatId, "‚ùå Ge√ßersiz komut. /yardim ile komutlarƒ± g√∂r√ºnt√ºleyin.");
        }
    }

    private int broadcastMessage(String message) {
        List<BotUser> allUsers = botUserRepository.findAll();
        int successCount = 0;

        for (BotUser user : allUsers) {
            try {
                SendMessage sendMessage = new SendMessage();
                sendMessage.setChatId(user.getChatId());
                sendMessage.setText("üì¢ Duyuru\n\n" + message);
                execute(sendMessage);
                successCount++;
                Thread.sleep(50); // Rate limiting i√ßin k√º√ß√ºk bir gecikme
            } catch (Exception e) {
                logger.error("Broadcast message failed for user: " + user.getChatId(), e);
            }
        }

        return successCount;
    }

    private void saveOrUpdateUser(Long chatId, User user) {
        BotUser botUser = botUserRepository.findById(chatId).orElse(new BotUser());
        botUser.setChatId(chatId);
        botUser.setUsername(user.getUserName());
        botUser.setFirstName(user.getFirstName());
        botUser.setLastName(user.getLastName());
        botUser.setLastInteractionDate(LocalDateTime.now());
        botUser.setLastActivityDate(LocalDateTime.now());

        // Yeni kullanƒ±cƒ± ise varsayƒ±lan deƒüerleri ayarla
        if (!botUserRepository.existsById(chatId)) {
            botUser.setNotificationsEnabled(true);
            botUser.setIsAdmin(false);  // Varsayƒ±lan olarak admin deƒüil
        }

        botUserRepository.save(botUser);
    }

    private void enableNotifications(Long chatId) throws TelegramApiException {
        BotUser user = botUserRepository.findById(chatId).orElseThrow();
        user.setNotificationsEnabled(true);
        botUserRepository.save(user);
        sendMessage(chatId, "üîî Bildirimler aktif edildi!\nHer g√ºn sabah 07:00'de g√ºn√ºn men√ºs√ºn√º size ileteceƒüim.");
    }

    private void disableNotifications(Long chatId) throws TelegramApiException {
        BotUser user = botUserRepository.findById(chatId).orElseThrow();
        user.setNotificationsEnabled(false);
        botUserRepository.save(user);
        sendMessage(chatId, "üîï Bildirimler kapatƒ±ldƒ±.");
    }

    private void sendWelcomeMessage(Long chatId) throws TelegramApiException {
        String message = """
                üëã KYK Yemek Men√ºs√º Botuna Ho≈ü Geldiniz!
                
                Bu bot ile g√ºnl√ºk yemek men√ºlerini takip edebilir ve bildirim alabilirsiniz.
                
                üîπ Komutlar:
                /bugun - Bug√ºn√ºn men√ºs√º
                /yarin - Yarƒ±nƒ±n men√ºs√º
                /bildirim_ac - G√ºnl√ºk bildirimleri a√ß
                /bildirim_kapat - Bildirimleri kapat
                /yardim - Komut listesi
                
                üí° ƒ∞pucu: Bildirimler varsayƒ±lan olarak a√ßƒ±ktƒ±r.
                """;
        sendMessage(chatId, message);
    }

    private void sendTodaysMeals(Long chatId) throws TelegramApiException {
        LocalDate today = LocalDate.now();
        List<Meal> meals = mealService.getMealsByDate(today);
        sendMealMessage(chatId, today, meals);
    }

    private void sendTomorrowsMeals(Long chatId) throws TelegramApiException {
        LocalDate tomorrow = LocalDate.now().plusDays(1);
        List<Meal> meals = mealService.getMealsByDate(tomorrow);
        sendMealMessage(chatId, tomorrow, meals);
    }

    private void sendMealMessage(Long chatId, LocalDate date, List<Meal> meals) throws TelegramApiException {
        if (meals.isEmpty()) {
            String formattedDate = date.format(DateTimeFormatter.ofPattern("dd MMMM yyyy", new Locale("tr")));
            sendMessage(chatId, "üìÖ " + formattedDate + " tarihine ait men√º hen√ºz yayƒ±nlanmamƒ±≈ü.\n\nMen√º yayƒ±nlandƒ±ƒüƒ±nda bildirim almak i√ßin /bildirim_ac komutunu kullanabilirsiniz.");
            return;
        }

        StringBuilder messageBuilder = new StringBuilder();
        String formattedDate = date.format(DateTimeFormatter.ofPattern("dd MMMM yyyy", new Locale("tr")));
        messageBuilder.append("üìÖ ").append(formattedDate).append("\n");
        messageBuilder.append("‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n\n");

        meals.stream()
                .filter(meal -> meal.getMealType() == 0)
                .findFirst()
                .ifPresent(breakfast -> {
                    formatMeal(messageBuilder, "üåÖ KAHVALTI", breakfast);
                    messageBuilder.append("\n");
                });

        meals.stream()
                .filter(meal -> meal.getMealType() == 1)
                .findFirst()
                .ifPresent(dinner -> formatMeal(messageBuilder, "üçΩÔ∏è AK≈ûAM YEMEƒûƒ∞", dinner));

        sendMessage(chatId, messageBuilder.toString());
    }

    private void formatMeal(StringBuilder builder, String title, Meal meal) {
        builder.append(title).append(" (").append(meal.getTotalCalories()).append(" kcal)").append("\n");
        builder.append("‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n");
        builder.append("‚úì ").append(meal.getFirst()).append(" (").append(meal.getFirstCalories()).append(" kcal)").append("\n");
        builder.append("‚úì ").append(meal.getSecond()).append(" (").append(meal.getSecondCalories()).append(" kcal)").append("\n");
        builder.append("‚úì ").append(meal.getThird()).append(" (").append(meal.getThirdCalories()).append(" kcal)").append("\n");
        builder.append("‚úì ").append(meal.getFourth()).append(" (").append(meal.getFourthCalories()).append(" kcal)").append("\n");
    }

    private void sendHelpMessage(Long chatId) throws TelegramApiException {
        String helpMessage = """
                ‚ÑπÔ∏è Komut Listesi
                
                üì± Men√º Komutlarƒ±:
                /bugun - Bug√ºnk√º men√ºy√º g√∂ster
                /yarin - Yarƒ±nki men√ºy√º g√∂ster
                
                üîî Bildirim Ayarlarƒ±:
                /bildirim_ac - G√ºnl√ºk bildirimleri aktif et
                /bildirim_kapat - Bildirimleri kapat
                
                ‚ùì Diƒüer:
                /yardim - Bu listeyi g√∂ster
                """ + (adminService.isAdmin(chatId) ? """
                
                üîß Y√∂netici Komutlarƒ±:
                /stats - Genel istatistikler
                /admin_list - Kullanƒ±cƒ± listesi
                /admin_broadcast - Toplu mesaj g√∂nder
                /admin_stats - Detaylƒ± analiz
                """ : "");
        sendMessage(chatId, helpMessage);
    }

    private void sendMessage(Long chatId, String text) throws TelegramApiException {
        SendMessage message = new SendMessage();
        message.setChatId(chatId);
        message.setText(text);
        message.enableHtml(true);
        execute(message);
    }
}